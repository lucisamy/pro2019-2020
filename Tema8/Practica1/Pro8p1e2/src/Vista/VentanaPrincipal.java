    public void validarDatoEntrada(String dato,String expr)throws DatoInvalidoException{
        Pattern p = Pattern.compile(expr);
            Matcher m = p.matcher(dato);
            if(!m.matches())
                throw new DatoInvalidoException(dato);
    }
