    private void bAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAceptarActionPerformed
        try{
            if(tfNombre.getText().isEmpty()||tfApellidos.getText().isEmpty()||tfDni.getText().isEmpty())
                throw new DatoObligatorioException();
            else{
            lAviso.setVisible(false);
            //recuadros rojos desaparecen? NO. cómo les quito el borde?
            tfNombre.setBorder(defaultBorder);
            tfApellidos.setBorder(defaultBorder);
            tfDni.setBorder(defaultBorder);
            
            validarDatoEntrada(tfNombre.getText(),"^[A-Z][a-z]{2,}$");
            validarDatoEntrada(tfApellidos.getText(),"^([A-Z][a-z]{2,}[ ]?)+$");
            validarDatoEntrada(tfDni.getText(),"^[0-9]{8}[ABCDEFGHJKLMNPQRSTVWXYZ]$");
            //validar dni: es la letra correcta?
            //pasamos al controlador todos los datos para dar de alta persona
            Pro8p1e2.altaPersonas(tfNombre.getText(),tfApellidos.getText(),tfDni.getText());
            }
        }
        catch(DatoObligatorioException e){
            lAviso.setVisible(true);
            if(tfNombre.getText().isEmpty())
                tfNombre.setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(255, 0, 0)));
            if(tfApellidos.getText().isEmpty())
                tfApellidos.setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(255, 0, 0)));
            if(tfDni.getText().isEmpty())
                tfDni.setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(255, 0, 0)));    
        }
        catch(DatoInvalidoException e){
            //mostrar recuadro rojo en dato incorrecto y mensaje personalizado.
            //mostrar label dato incorrecto correspondiente
            switch(e.getCampo()){
                case "tfNombre": lNombre.setVisible(true);
                                 lNombre.setText(e.getMessage());
                    break;
                case "tfApellidos":lApellidos.setVisible(true);
                                    lApellidos.setText(e.getMessage());
                    break;
                case "tfDni":lDni.setVisible(true);
                             lDni.setText(e.getMessage());
            }
            
            //lAviso.setVisible(true);
        }
    }//GEN-LAST:event_bAceptarActionPerformed

